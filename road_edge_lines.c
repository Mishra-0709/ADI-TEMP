// Gaussian Blur - Approximation with 5x5 Gaussian kernel
void gaussian_blur(unsigned int image[HEIGHT][WIDTH], unsigned int result[HEIGHT][WIDTH]) {
    int kernel[GAUSSIAN_KERNEL_SIZE][GAUSSIAN_KERNEL_SIZE] = {
        {1, 4, 7, 4, 1},
        {4, 16, 26, 16, 4},
        {7, 26, 41, 26, 7},
        {4, 16, 26, 16, 4},
        {1, 4, 7, 4, 1}
    };
    int sumKernel = 273;

    for (int i = 2; i < HEIGHT - 2; i++) {
        for (int j = 2; j < WIDTH - 2; j++) {
            int sum = 0;
            for (int ki = -2; ki <= 2; ki++) {
                for (int kj = -2; kj <= 2; kj++) {
                    sum += image[i + ki][j + kj] * kernel[ki + 2][kj + 2];
                }
            }
            result[i][j] = sum / sumKernel;
        }
    }
}

// Gradient Magnitude and Direction calculation using Sobel operator
void gradient_magnitude_direction(unsigned int image[HEIGHT][WIDTH], float gradient[HEIGHT][WIDTH], float direction[HEIGHT][WIDTH]) {
    int gx, gy;
    int sobel_x[3][3] = {{-1, 0, 1}, {-2, 0, 2}, {-1, 0, 1}};
    int sobel_y[3][3] = {{1, 2, 1}, {0, 0, 0}, {-1, -2, -1}};

    for (int i = 1; i < HEIGHT - 1; i++) {
        for (int j = 1; j < WIDTH - 1; j++) {
            gx = 0;
            gy = 0;
            for (int ki = -1; ki <= 1; ki++) {
                for (int kj = -1; kj <= 1; kj++) {
                    gx += image[i + ki][j + kj] * sobel_x[ki + 1][kj + 1];
                    gy += image[i + ki][j + kj] * sobel_y[ki + 1][kj + 1];
                }
            }
            gradient[i][j] = sqrt(gx * gx + gy * gy);
            direction[i][j] = atan2(gy, gx);
        }
    }
}

// Non-maximum suppression
void non_maximum_suppression(float gradient[HEIGHT][WIDTH], float direction[HEIGHT][WIDTH], unsigned int result[HEIGHT][WIDTH]) {
    for (int i = 1; i < HEIGHT - 1; i++) {
        for (int j = 1; j < WIDTH - 1; j++) {
            float angle = direction[i][j] * 180.0 / M_PI;
            angle = (angle < 0) ? angle + 180 : angle;

            if ((angle >= 0 && angle < 22.5) || (angle >= 157.5 && angle <= 180)) {
                if (gradient[i][j] >= gradient[i][j - 1] && gradient[i][j] >= gradient[i][j + 1])
                    result[i][j] = gradient[i][j];
            } else if (angle >= 22.5 && angle < 67.5) {
                if (gradient[i][j] >= gradient[i - 1][j + 1] && gradient[i][j] >= gradient[i + 1][j - 1])
                    result[i][j] = gradient[i][j];
            } else if (angle >= 67.5 && angle < 112.5) {
                if (gradient[i][j] >= gradient[i - 1][j] && gradient[i][j] >= gradient[i + 1][j])
                    result[i][j] = gradient[i][j];
            } else if (angle >= 112.5 && angle < 157.5) {
                if (gradient[i][j] >= gradient[i - 1][j - 1] && gradient[i][j] >= gradient[i + 1][j + 1])
                    result[i][j] = gradient[i][j];
            } else {
                result[i][j] = 0;
            }
        }
    }
}

// Double threshold and edge tracking by hysteresis
void double_threshold(unsigned int image[HEIGHT][WIDTH], unsigned int result[HEIGHT][WIDTH], int low, int high) {
    for (int i = 0; i < HEIGHT; i++) {
        for (int j = 0; j < WIDTH; j++) {
            if (image[i][j] >= high)
                result[i][j] = 255;
            else if (image[i][j] >= low)
                result[i][j] = 128;
            else
                result[i][j] = 0;
        }
    }
}

// Hysteresis to finalize edges
void hysteresis(unsigned int image[HEIGHT][WIDTH], unsigned int result[HEIGHT][WIDTH]) {
    for (int i = 1; i < HEIGHT - 1; i++) {
        for (int j = 1; j < WIDTH - 1; j++) {
            if (image[i][j] == 128) {
                if (image[i - 1][j - 1] == 255 || image[i - 1][j] == 255 || image[i - 1][j + 1] == 255 ||
                    image[i][j - 1] == 255 || image[i][j + 1] == 255 ||
                    image[i + 1][j - 1] == 255 || image[i + 1][j] == 255 || image[i + 1][j + 1] == 255) {
                    result[i][j] = 255;
                } else {
                    result[i][j] = 0;
                }
            }
        }
    }
}

float probabilistic_hough_transform(unsigned int edges[HEIGHT][WIDTH], LineSegment *lines, int max_lines) {
    int num_lines = 0;
    srand(time(0));

    LineSegment left_line;
    LineSegment right_line;

    left_line.x1 = 0;
    left_line.x2 = 0;
    left_line.y1 = 0;
    left_line.y2 = 0;
    right_line.x1 = 0;
    right_line.x2 = 0;
    right_line.y1 = 0;
    right_line.y2 = 0;


    for (int y = 30; y < HEIGHT-5; y++) {
        for (int x = 5; x < WIDTH-5; x++) {
            if (edges[y][x] == 255 && rand()%64 < EDGE_THRESHOLD) {
                for (int theta = 0; theta < 180; theta += THETA_RES) {
                    double rad = theta * M_PI / 180.0;
                    float dx = cos(rad), dy = sin(rad);
                    float x1 = x, y1 = y;
                    float x2 = x, y2 = y;
                    // Extend the line in the positive direction
                    while ((int)x1 >= 0 && (int)x1 < WIDTH && (int)y1 >= 0 && (int)y1 < HEIGHT && edges[(int)y1][(int)x1] == 255) {
                        x1 += dx;
                        y1 += dy;
                    }

                    // Extend the line in the negative direction
                    while ((int)x2 >= 0 && (int)x2 < WIDTH && (int)y2 >= 0 && (int)y2 < HEIGHT && edges[(int)y2][(int)x2] == 255) {
                        x2 -= dx;
                        y2 -= dy;
                    }

                    // Calculate length and store line if it exceeds MIN_LINE_LENGTH
                    int length = (int)sqrt((x2 - x1) * (x2 - x1) + (y2 - y1) * (y2 - y1));
                    float angle = (y2-y1)/(x2-x1); //i know it's tan but who cares
                    if (length >= MIN_LINE_LENGTH && num_lines < max_lines) {
                        lines[num_lines++] = (LineSegment){(int)x1, (int)y1, (int)x2, (int)y2};
                        float curr_left_len = (int)sqrt((left_line.x2 - left_line.x1) * (left_line.x2 - left_line.x1) + (left_line.y2 - left_line.y1) * (left_line.y2 - left_line.y1));
                        float curr_right_len = (int)sqrt((right_line.x2 - right_line.x1) * (right_line.x2 - left_line.x1) + (right_line.y2 - left_line.y1) * (right_line.y2 - right_line.y1));
                        if(angle>0 && angle<20 && length>curr_left_len){
                            left_line = (LineSegment){(int)x1, (int)y1, (int)x2, (int)y2};
                        }
                        else if(angle<0 && angle>-20 && length>curr_right_len){
                            right_line = (LineSegment){(int)x1, (int)y1, (int)x2, (int)y2};
                        }
                    }
                }
            }
        }
    }
    // for (int i = 0; i < num_lines; i++) {
    //     printf("Line %d: (%d, %d) -> (%d, %d)\n", i, lines[i].x1, lines[i].y1, lines[i].x2, lines[i].y2);
    // }

    printf("right line: (%d ,%d) ,(%d ,%d) \n", left_line.x1,left_line.y1,left_line.x2,left_line.y2);
    printf("left line: (%d ,%d) ,(%d ,%d) \n", right_line.x1,right_line.y1,right_line.x2,right_line.y2);

    float left_angle = (float)(left_line.y2 - left_line.y1) / (left_line.x2 - left_line.x1);
    float right_angle = (float)(right_line.y2 - right_line.y1) / (right_line.x2 - right_line.x1);

    float sum = left_angle + right_angle ;

    return sum;
}

